macro_rules! impl_small_gf {
    ($field_name:ident, $base_type:ty) => {
        impl BytesRepr for $field_name {
            type Repr = [u8; (<$base_type>::BITS / 8) as usize];
            #[inline(always)]
            fn to_repr(self) -> Self::Repr {
                self.0.to_le_bytes()
            }
            #[inline(always)]
            fn from_repr(repr: Self::Repr) -> Self {
                Self(<$base_type>::from_le_bytes(repr))
            }
        }

        #[allow(clippy::suspicious_arithmetic_impl)]
        impl Add for $field_name {
            type Output = Self;
            #[inline(always)]
            fn add(self, other: Self) -> Self::Output {
                Self(self.0 ^ other.0)
            }
        }

        impl Mul for $field_name {
            type Output = Self;
            #[inline(always)]
            fn mul(self, other: Self) -> Self::Output {
                // not constant time, but decent (and currently best) overall
                if self.0 == 0 || other.0 == 0 {
                    Self(0)
                } else {
                    Self::ANTI_LOG_TABLE[((Self::LOG_TABLE[self.0 as usize] as u32
                        + Self::LOG_TABLE[other.0 as usize] as u32)
                        % (Self::ORDER as u32 - 1)) as usize]
                }
            }
        }

        impl ConstantTimeEq for $field_name {
            #[inline(always)]
            fn ct_eq(&self, other: &Self) -> Choice {
                self.0.ct_eq(&other.0)
            }
        }
        impl ConditionallySelectable for $field_name {
            #[inline(always)]
            fn conditional_select(a: &Self, b: &Self, choice: Choice) -> Self {
                Self(<$base_type>::conditional_select(&a.0, &b.0, choice))
            }
        }

        impl SmallGF for $field_name {
            const ORDER: usize = Self::ORDER;
            const LOG_ORDER: u32 = Self::LOG_ORDER;
        }

        impl Field for $field_name {
            const ZERO: Self = Self(0);
            const ONE: Self = Self(1);

            #[inline(always)]
            fn random(mut rng: impl RngCore) -> Self {
                Self(rng.gen::<$base_type>() & Self::BIT_MASK)
            }

            #[inline(always)]
            fn square(&self) -> Self {
                *self * self
            }

            #[inline(always)]
            fn double(&self) -> Self {
                *self + self
            }

            #[inline(always)]
            fn invert(&self) -> CtOption<Self> {
                let mut x = *self;
                let mut y = Self::ONE;
                x = x.square();
                for _ in 0..Self::LOG_ORDER - 2 {
                    y = x * y;
                    x = x.square();
                }
                CtOption::new(x * y, !self.ct_eq(&Self::ZERO))
            }

            fn sqrt_ratio(_num: &Self, _div: &Self) -> (Choice, Self) {
                panic!("not implemented")
            }
        }

        impl From<$base_type> for $field_name {
            fn from(val: $base_type) -> Self {
                Self(val)
            }
        }

        impl From<$field_name> for $base_type {
            fn from(val: $field_name) -> Self {
                val.0
            }
        }

        impl From<usize> for $field_name {
            fn from(val: usize) -> Self {
                debug_assert!(val <= <$base_type>::MAX as usize);
                Self(val as $base_type)
            }
        }

        impl From<$field_name> for usize {
            fn from(val: $field_name) -> Self {
                val.0 as usize
            }
        }

        impl_additional_field_arithmetic!($field_name);
    };
}

